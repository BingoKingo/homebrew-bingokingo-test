cask "font-swei-b2-sans-sc" do
  version "2.128"
  sha256 :no_check

  url "https://github.com/max32002/swei-b2-sans.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK SC"
  name "Swei B2 Sans CJK SC"
  name "獅尾B2黑體SC"
  homepage "https://max-everyday.com/2020/07/swei-b2-sans/"

  depends_on formula: "fonttools"

  font "SweiB2SansCJKsc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK SC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK SC/SweiB2SansCJKsc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
