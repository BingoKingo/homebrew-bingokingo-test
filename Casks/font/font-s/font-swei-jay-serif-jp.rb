cask "font-swei-jay-serif-jp" do
  version "1.0"
  sha256 :no_check

  url "https://github.com/max32002/swei-jay-serif.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK JP"
  name "Swei Jay Serif CJK JP"
  name "獅尾簡中宋體JP"
  homepage "https://max-everyday.com/2020/11/swei-jay-serif/"

  depends_on formula: "fonttools"

  font "SweiJaySerifCJKjp.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK JP/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK JP/SweiJaySerifCJKjp.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
