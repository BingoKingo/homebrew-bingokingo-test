cask "font-swei-spring-jp" do
  version "1.061"
  sha256 :no_check

  url "https://github.com/max32002/swei-spring.git",
      verified:  "github.com/",
      branch:    "main",
      only_path: "CJK JP"
  name "Swei Spring CJK JP"
  name "獅尾四季春JP"
  homepage "https://max-everyday.com/2020/04/swei-spring/"

  depends_on formula: "fonttools"

  font "SweiSpringCJKjp.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK JP/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK JP/SweiSpringCJKjp.ttc'
      SHELL
    ]
  end

