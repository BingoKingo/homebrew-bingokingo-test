cask "font-swei-xd-pearl-jp" do
  version "2.129"
  sha256 :no_check

  url "https://github.com/max32002/swei-xd.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK JP"
  name "Swei XD Pearl CJK JP"
  name "獅尾XD珍珠JP"
  homepage "https://max-everyday.com/2020/07/swei-xd-pearl/"

  depends_on formula: "fonttools"

  font "SweiXDPearlCJKjp.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK JP/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK JP/SweiXDPearlCJKjp.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
