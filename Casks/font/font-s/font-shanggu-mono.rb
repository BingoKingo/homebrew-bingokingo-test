cask "font-shanggu-mono" do
  version "1.027"
  sha256 "435bc519f9d56d9829ba4fdd1cd188850f1e65330e0a11a31b6c7b1ff6bfcfd7"

  url "https://github.com/GuiWonder/Shanggu/releases/download/#{version}/ShangguMonoTTCs.7z"
  name "Shangu Mono"
  name "尙古等宽"
  homepage "https://github.com/GuiWonder/Shanggu/"

  livecheck do
    cask "font-shanggu-sans"
  end

  font "ShangguMono.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/'*.ttc \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/ShangguMono.ttc'
      SHELL
    ]
  end

  caveats do
