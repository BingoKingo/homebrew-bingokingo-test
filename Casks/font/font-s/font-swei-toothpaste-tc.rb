cask "font-swei-toothpaste-tc" do
  version "1.0"
  sha256 :no_check

  url "https://github.com/max32002/swei-toothpaste.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK TC"
  name "Swei Toothpaste CJK TC"
  name "獅尾牙膏圓體"
  homepage "https://max-everyday.com/2020/04/swei-spring/"

  depends_on formula: "fonttools"

  font "SweiToothpasteCJKtc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK TC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK TC/SweiToothpasteCJKtc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
