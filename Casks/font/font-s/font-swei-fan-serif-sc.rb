cask "font-swei-fan-serif-sc" do
  version "1.0"
  sha256 :no_check

  url "https://github.com/max32002/swei-fan-serif.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK SC"
  name "Swei Fan Serif CJK SC"
  name "獅尾繁中宋體SC"
  homepage "https://max-everyday.com/2020/11/swei-fan-serif/"

  depends_on formula: "fonttools"

  font "SweiFanSerifCJKsc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK SC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK SC/SweiFanSerifCJKsc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
