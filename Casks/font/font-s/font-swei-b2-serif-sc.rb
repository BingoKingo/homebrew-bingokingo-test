cask "font-swei-b2-serif-sc" do
  version "1.0"
  sha256 :no_check

  url "https://github.com/max32002/swei-b2-serif.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK SC"
  name "Swei B2 Serif CJK SC"
  name "獅尾B2宋朝SC"
  homepage "https://max-everyday.com/2020/07/swei-b2-serif/"

  depends_on formula: "fonttools"

  font "SweiB2SerifCJKsc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK SC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK SC/SweiB2SerifCJKsc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
