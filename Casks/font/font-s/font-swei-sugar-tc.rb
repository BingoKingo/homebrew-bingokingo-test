cask "font-swei-sugar-tc" do
  version "1.0"
  sha256 :no_check

  url "https://github.com/max32002/swei-sugar.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK TC"
  name "Swei Sugar CJK TC"
  name "獅尾加糖宋體"
  homepage "https://max-everyday.com/2020/11/swei-sugar/"

  depends_on formula: "fonttools"

  font "SweiSugarCJKtc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK TC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK TC/SweiSugarCJKtc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
