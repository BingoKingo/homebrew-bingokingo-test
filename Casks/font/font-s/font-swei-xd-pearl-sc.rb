cask "font-swei-xd-pearl-sc" do
  version "2.129"
  sha256 :no_check

  url "https://github.com/max32002/swei-xd.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK SC"
  name "Swei XD Pearl CJK SC"
  name "獅尾XD珍珠SC"
  homepage "https://max-everyday.com/2020/07/swei-xd-pearl/"

  depends_on formula: "fonttools"

  font "SweiXDPearlCJKsc.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK SC/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK SC/SweiXDPearlCJKsc.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
