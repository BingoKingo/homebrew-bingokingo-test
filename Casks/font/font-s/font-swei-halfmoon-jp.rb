cask "font-swei-halfmoon-jp" do
  version "2.070"
  sha256 :no_check

  url "https://github.com/max32002/swei-halfmoon.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "CJK JP"
  name "Swei Half Moon CJK JP"
  name "獅尾半月JP"
  homepage "https://max-everyday.com/2020/04/swei-half-moon/"

  depends_on formula: "fonttools"

  font "SweiHalfMoonCJKjp.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/CJK JP/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/CJK JP/SweiHalfMoonCJKjp.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
